namespace SpaceStation.Service
{
    /// <summary>
    /// Local Service
    /// </summary>
    public class LocalService
    {
        public int Add(int a, int b)
        {
            return a + b;
        }

        public double Mul(double a, double b)
        {
            return a * b;
        }

        public double Squa(double a)
        {
            return a * a;
        }
    }
}